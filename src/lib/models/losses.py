def _compute_angle_loss(vec1,vec2):
  #inner_product = (vec1*vec2).sum(dim=3)
  #norm                    
  vec1_norm = torch.norm(vec1,dim=3) #32,10,8
  vec2_norm = torch.norm(vec2,dim=3) #32,10,8
  #product
  vec1_dot_vec2 = (vec1*vec2).sum(dim=3) # vec1_norm * vec2_norm
  #rad  
  all_cos_theta = vec1_dot_vec2/(vec1_norm*vec2_norm+1e-4)
  pos_inds = torch.abs(all_cos_theta).sum(axis=2)!=0 #>0不可行，会滤掉负值
  cos_theta = torch.clamp(all_cos_theta[pos_inds],min=-1+1e-7,max=1-1e-7)
  rad = torch.acos(cos_theta)
  degree = rad*180/math.pi  
  num_pos = pos_inds.float().sum()*4 # 4 vectors
  gt_degree = torch.ones(degree.size()[0],degree.size()[1]).to('cuda')*180
  degree_error = F.smooth_l1_loss(degree,gt_degree,size_average=False)
  loss = degree_error.sum()/num_pos+1e-4
  return loss

class MiddleAngleLoss(nn.Module):
  def __init__(self):
    super(MiddleAngleLoss, self).__init__()
    
  def forward(self, output, mask, ind):
    mask = mask.reshape(mask.shape[0],mask.shape[1],8,2).float()
    pred = _transpose_and_gather_feat(output,ind).reshape(mask.shape[0],mask.shape[1],8,2)
    pred = pred*mask
               
    pred_mp_inds = torch.arange(1,8,2)    
    pred_mp_vec1 = pred[..., pred_mp_inds, :] - pred[...,(pred_mp_inds-1)%8, :]
    pred_mp_vec2 = pred[..., pred_mp_inds, :] - pred[...,(pred_mp_inds+1)%8, :]
    
    loss = _compute_angle_loss(pred_mp_vec1, pred_mp_vec2)
    loss = loss + 1e-4
    return loss
  
def _slow_neg_loss(pred, gt):
  '''focal loss from CornerNet'''
  pos_inds = gt.eq(1)
  neg_inds = gt.lt(1)

  neg_weights = torch.pow(1 - gt[neg_inds], 4)

  loss = 0
  pos_pred = pred[pos_inds]
  neg_pred = pred[neg_inds]

  pos_loss = torch.log(pos_pred) * torch.pow(1 - pos_pred, 2)
  neg_loss = torch.log(1 - neg_pred) * torch.pow(neg_pred, 2) * neg_weights

  num_pos  = pos_inds.float().sum()
  pos_loss = pos_loss.sum()
  neg_loss = neg_loss.sum()

  if pos_pred.nelement() == 0:
    loss = loss - neg_loss
  else:
    loss = loss - (pos_loss + neg_loss) / num_pos
  return loss


def _neg_loss(pred, gt):
  ''' Modified focal loss. Exactly the same as CornerNet.
      Runs faster and costs a little bit more memory
    Arguments:
      pred (batch x c x h x w)
      gt_regr (batch x c x h x w)
  '''
  pos_inds = gt.eq(1).float()
  neg_inds = gt.lt(1).float()

  neg_weights = torch.pow(1 - gt, 4)

  loss = 0

  pos_loss = torch.log(pred) * torch.pow(1 - pred, 2) * pos_inds
  neg_loss = torch.log(1 - pred) * torch.pow(pred, 2) * neg_weights * neg_inds
  
 
  num_pos  = pos_inds.float().sum()
  pos_loss = pos_loss.sum()
  neg_loss = neg_loss.sum()

  if num_pos == 0:
    loss = loss - neg_loss
  else:
    loss = loss - (pos_loss + neg_loss) / num_pos
  return loss

